# операции с множествами 

a = {1, 2, 3, 4}
b = {2, 5, 4, 8}
c = a.copy() # копирование множества c = {1, 2, 3, 4}
u = a.union(b) # объединение множеств u = {1, 2, 3, 4, 5, 8}
i = a.intersection(b) # пересечение - вывод только элементов, которые есть в обоих множествах i = {2, 4}
dr = a.difference(b) # вычитание множеств - из множества а убираем значения, которые есть в b / dr = {1, 3}
print(dr)

