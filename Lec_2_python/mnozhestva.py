# обозначение множеств
colors = {'yellow', 'red', 'blue'}

# добавление элемента (должно быть уникальным)
colors.add('white')
print(colors)

# удаление элемента
colors.remove('red')
print(colors)

# проверка, есть ли значение в множестве
colors.discard('white')
print(colors)

# удаление всех элементов
colors.clear()
print(colors)

